function toggle() {

    var ele = document.getElementById("customer");
    var ele1 = document.getElementById("expert");
    if(ele.style.display == "block") {

        ele.style.display = "none";
        ele1.style.display = "block";
    }
    else {

        ele.style.display = "block";
        ele1.style.display = "none";
    }
}
